/** Lookup distance between two location IDs; defaults to 5 km if unknown */
export function getDistance(fromId, toId) {
  if (fromId === toId) return 0;
  return (
    locationDistances[`${fromId}-${toId}`] ||
    locationDistances[`${toId}-${fromId}`] ||
    5.0
  );
}

/** Calculate fare: baseFare + distance × perKm, scaled by sharing multiplier */
export function calculateFare(vehicle, sharingOption, distance) {
  return Math.ceil((vehicle.baseFare + distance * vehicle.perKm) * sharingOption.multiplier);
}

/** Pick a random mock driver */
